  function getLang() {
    const stored = localStorage.getItem(LANG_KEY);
    if (stored && T[stored]) return stored;
    const browser = (navigator.language || '').split('-')[0].toLowerCase();
    if (T[browser]) return browser;
    return 'de';
  }

  function applyLang(lang) {
    const t = T[lang] || T.de;
    document.documentElement.lang = lang;
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      const key = el.getAttribute('data-i18n');
      if (t[key] !== undefined) el.textContent = t[key];
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-ph');
      if (t[key] !== undefined) el.placeholder = t[key];
    });
    const sel = document.getElementById('appLangSelect');
    if (sel) sel.value = lang;
    document.dispatchEvent(new CustomEvent('app:language-changed', { detail: { lang: lang } }));
  }

  function t(key, vars) {
    const lang = getLang();
    const dict = T[lang] || T.de;
    let value = dict[key];
    if (value === undefined) value = T.de[key];
    if (value === undefined) return key;
    if (!vars || typeof value !== 'string') return value;
    return value.replace(/\{(\w+)\}/g, function (_, name) {
      const replacement = vars[name];
      return replacement === undefined || replacement === null ? '' : String(replacement);
    });
  }

  window.AppI18n = { T: T, getLang: getLang, applyLang: applyLang, t: t, LANG_KEY: LANG_KEY };

  document.addEventListener('DOMContentLoaded', function () {
    const lang = getLang();
    applyLang(lang);
    const sel = document.getElementById('appLangSelect');
    if (sel) {
      sel.value = lang;
      sel.addEventListener('change', function (e) {
        const l = e.target.value;
        localStorage.setItem(LANG_KEY, l);
        applyLang(l);
      });
    }
  });
